/**
 * Get filtered words based on selected categories and difficulty
 */
export function getFilteredWords(
  categories: Category[],
  difficulty: Difficulty
): Word[] {
  const words: Word[] = [];

  categories.forEach(category => {
    const categoryWords = WORDS_BY_CATEGORY[category]?.[difficulty] || [];
    words.push(...categoryWords);
  });

  return words;
}

/**
 * Get a random word from filtered set
 */
export function getRandomWord(
  categories: Category[],
  difficulty: Difficulty
): Word {
  const filtered = getFilteredWords(categories, difficulty);

  if (filtered.length === 0) {
    throw new Error(`No words found for categories: ${categories.join(', ')} and difficulty: ${difficulty}`);
  }

  const randomIndex = Math.floor(Math.random() * filtered.length);
  return filtered[randomIndex];
}

/**
 * Get a random role from a word (only for locations category)
 */
export function getRandomRole(word: Word, usedRoles: string[] = []): string {
  if (!word.roles || word.roles.length === 0) {
    throw new Error('This word does not have roles');
  }

  const availableRoles = word.roles.filter(role => !usedRoles.includes(role));
  if (availableRoles.length === 0) {
    // If all roles are used, just pick a random one
    return word.roles[Math.floor(Math.random() * word.roles.length)];
  }

  const randomIndex = Math.floor(Math.random() * availableRoles.length);
  return availableRoles[randomIndex];
}

// Backward compatibility: export the old function names
export const getRandomLocation = (categories: Category[] = ['locations'], difficulty: Difficulty = 'medium') =>
  getRandomWord(categories, difficulty);
